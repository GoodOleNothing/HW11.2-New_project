def str_f():
    user = input()
    return print(f'{user.upper()}')
"""Я ДРУГОЙ ПРОГРАММИСТ, ВНОШУ НЕПРАВИЛЬНЫЕ ИЗМЕНЕНИЯ В ОПИСАНИЕ ФУНКЦИИ!"""


str_f()

def str_t():
    user = input()
    return print(f'{user.title()}')
"""МЕНЯЮ НЕПРАВИЛЬНЫЙ ДОКСТРИНГ"""

str_t()
